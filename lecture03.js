//Keys

const array = ["a", "b", "c"];

const keys = array.keys();
const values = array.values();
const entries = array.entries();

console.log(keys);
console.log(keys.next());
console.log(keys.next());
console.log(values);
console.log(values.next());
console.log(entries);
console.log(entries.next());
console.log(entries.next());